#!/usr/bin/env node

const fs = require("fs");
const path = require("path");
const { execFileSync } = require("child_process");

const root = path.resolve(__dirname, "..");
const contextDir = path.join(root, "context");
const handoffPath = path.join(contextDir, "CURRENT_HANDOFF.md");
const sessionLogPath = path.join(contextDir, "SESSION_LOG.md");

function run(command, args, options = {}) {
  try {
    return execFileSync(command, args, {
      cwd: root,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
      timeout: options.timeout ?? 30_000,
    }).trim();
  } catch (error) {
    const stderr = error?.stderr ? String(error.stderr).trim() : "";
    const stdout = error?.stdout ? String(error.stdout).trim() : "";
    const detail = stderr || stdout || error?.message || "unknown error";
    return `[failed: ${command} ${args.join(" ")}]\n${detail}`;
  }
}

function truncate(value, maxChars) {
  if (!value) return "";
  if (value.length <= maxChars) return value;
  return `${value.slice(0, maxChars)}\n... [truncated ${value.length - maxChars} chars]`;
}

function parseArgs() {
  const args = process.argv.slice(2);
  return {
    boot: args.includes("--boot"),
    noAppend: args.includes("--no-append"),
    note: process.env.MEMORY_NOTE || args.filter((arg) => !arg.startsWith("--")).join(" ").trim(),
  };
}

function printBootOrder() {
  const files = [
    "context/MEMORY_PROTOCOL.md",
    "context/CURRENT_HANDOFF.md",
    "context/MEMORIES.md",
    "context/PROJECT_CONTEXT.md",
    "context/PROGRESS.md",
    "context/SESSION_LOG.md",
    "context/CODEX_HANDOFF.md",
    "AGENTS.md",
  ];
  console.log("Read these before work:");
  for (const file of files) {
    console.log(`- ${file}`);
  }
  console.log("");
  console.log("Then check:");
  console.log("- git status -sb");
  console.log("- git remote -v");
  console.log("- git log --oneline --decorate -8");
  console.log("- gh repo view 00qq6868-del/ai-prompt-generator --json nameWithOwner,defaultBranchRef,url,visibility,updatedAt,pushedAt");
  console.log("- gh run list --repo 00qq6868-del/ai-prompt-generator --limit 8");
}

function main() {
  const { boot, noAppend, note } = parseArgs();
  if (boot) {
    printBootOrder();
    return;
  }

  fs.mkdirSync(contextDir, { recursive: true });

  const now = new Date().toISOString();
  const status = run("git", ["status", "-sb"]);
  const remote = run("git", ["remote", "-v"]);
  const recentCommits = run("git", ["log", "--oneline", "--decorate", "-8"]);
  const diffStat = run("git", ["diff", "--stat"]);
  const ghRepo = run("gh", [
    "repo",
    "view",
    "00qq6868-del/ai-prompt-generator",
    "--json",
    "nameWithOwner,defaultBranchRef,url,visibility,updatedAt,pushedAt",
  ]);
  const ghRuns = run("gh", [
    "run",
    "list",
    "--repo",
    "00qq6868-del/ai-prompt-generator",
    "--limit",
    "8",
    "--json",
    "databaseId,displayTitle,status,conclusion,headSha,createdAt,workflowName,url",
  ]);

  const content = `# Current Handoff

> Last updated: ${now}
> Generated by \`npm run memory:checkpoint\`.
> Read this first after any context compression or new AI session.

## User-Critical Rules

- Always restore memory before work. Read \`context/MEMORY_PROTOCOL.md\` first.
- Project root: \`E:\\AIWB\\ai-prompt-generator-codex\`.
- GitHub repo: \`https://github.com/00qq6868-del/ai-prompt-generator\`.
- Do not upload the AI 工作台 root to GitHub.
- User wants execution, validation, and persistent memory, not repeated explanations.
- Current corrected product requirement: generator model and evaluator model are one combined user choice. No separate evaluator picker should be required in the main UI. Selected generation/evaluation model ids must be mirrored into evaluator ids for backend compatibility.

## Current Note

${note || "No extra note provided."}

## Git Status

\`\`\`text
${truncate(status, 4_000)}
\`\`\`

## Remotes

\`\`\`text
${truncate(remote, 4_000)}
\`\`\`

## Recent Commits

\`\`\`text
${truncate(recentCommits, 4_000)}
\`\`\`

## Diff Stat

\`\`\`text
${truncate(diffStat || "No working tree diff.", 4_000)}
\`\`\`

## GitHub Repo

\`\`\`json
${truncate(ghRepo, 6_000)}
\`\`\`

## Recent GitHub Runs

\`\`\`json
${truncate(ghRuns, 8_000)}
\`\`\`

## Required Start Procedure For Next AI

1. Read \`context/MEMORY_PROTOCOL.md\`.
2. Read this file completely.
3. Read \`context/MEMORIES.md\`, \`context/PROJECT_CONTEXT.md\`, \`context/PROGRESS.md\`, latest \`context/SESSION_LOG.md\`, latest \`context/CODEX_HANDOFF.md\`, and \`AGENTS.md\`.
4. Run Git/GitHub status checks before editing.
5. Continue from the newest user request, not from stale plans.
`;

  fs.writeFileSync(handoffPath, content, "utf8");

  if (!noAppend) {
    const logEntry = `

## ${now} — Memory Checkpoint

- Updated \`context/CURRENT_HANDOFF.md\` with current Git/GitHub state.
- Note: ${note || "No extra note provided."}
- Working tree status summary:

\`\`\`text
${truncate(status, 2_000)}
\`\`\`
`;
    fs.appendFileSync(sessionLogPath, logEntry, "utf8");
  }

  console.log(`Updated ${path.relative(root, handoffPath)}`);
  if (!noAppend) console.log(`Appended ${path.relative(root, sessionLogPath)}`);
}

main();
